// 1. Kalkulator Hitung Luas Segitiga

// Luas Alas:
let alas = 10;

// Tinggi:
let tinggi = 5;

// Rumus:
let luasSegitiga = 0.5 * alas * tinggi;

// Luas Segitiga:
console.log("Luas Segitiga: " + luasSegitiga);

// 2. Kalkulator Hitung Luas Lingkaran

// Jari-jari lingkaran:
let jariJari = 7;

// Rumus:
let luasLingkaran = Math.PI * Math.pow(jariJari, 2);

// Luas Lingkaran:
console.log("Luas Lingkaran: " + luasLingkaran);

// 3. Kalkulator Hitung Luas Persegi Panjang

// Panjang Persegi Panjang:
let panjang = 15;

// Lebar Persegi Panjang:
let lebar = 13;

// Rumus:
let luasPersegiPanjang = panjang * lebar;

// Luas Persegi Panjang:
console.log("Luas Persegi Panjang: " + luasPersegiPanjang);

